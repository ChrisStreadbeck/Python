tags = ['pyton', 'development', 'tutorials', 'code']

tag_range = tags[1:2] #goes from index 1 and up to 2
tag_range2 = tags[2:3] #goes from index 2 and stops before index3
tag_range3 = tags[:2] #will bring back everything before index 2
tag_range4 = tags[2:] #will bring back everything after index 2
tag_range5 = tags[:-1] #will bring back everything but the last index
tag_range6 = tags[:] #will bring back everything
tag_range7 = tags[1:-1] #bring back all but the first and last index

print(tag_range)
print(tag_range2)
print(tag_range3)
print(tag_range4)
print(tag_range5)
print(tag_range6)
print(tag_range7)